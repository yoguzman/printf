#include <stdlib.h>

char		*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s = s + 1;
	}
	if (*s == c)
		return ((char *)s);
	return (NULL);
}
